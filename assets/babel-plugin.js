const fs = require('fs');
const path = require('path');
const nodeResolve = require('enhanced-resolve');

function readImportMapFile(explicitPath, webModulesFile) {
  if (explicitPath) {
    if (path.isAbsolute(explicitPath)) {
      return fs.readFileSync(explicitPath, {encoding: 'utf8'});
    }
    const explicitImportMap = path.join(webModulesFile, explicitPath);
    return fs.readFileSync(explicitImportMap, {encoding: 'utf8'});
  }
  const localImportMap = path.join(webModulesFile, `import-map.local.json`);
  const defaultImportMap = path.join(webModulesFile, `import-map.json`);
  try {
    return fs.readFileSync(localImportMap, {encoding: 'utf8'});
  } catch (err) {
    // do nothing
  }
  try {
    return fs.readFileSync(defaultImportMap, {encoding: 'utf8'});
  } catch (err) {
    // do nothing
  }
  throw new Error(`Import map not found. Run Snowpack first to generate one.
  ✘ ${localImportMap}
  ✘ ${defaultImportMap}`);
}

function getImportMap(explicitPath, webModulesFile) {
  const fileContents = readImportMapFile(explicitPath, webModulesFile);
  importMapJson = JSON.parse(fileContents);
  return importMapJson;
}

function rewriteImport(
  importMap,
  imp,
  file,
  webModulesFile,
  webModulesWeb,
  shouldResolveRelative,
  resolveDependency,
) {
  const isSourceImport = imp.startsWith('/') || imp.startsWith('.') || imp.startsWith('\\');
  const isRemoteImport = imp.startsWith('http://') || imp.startsWith('https://');
  const mappedImport = importMap.imports[imp];
  if (mappedImport) {
    if (mappedImport.startsWith('http://') || mappedImport.startsWith('https://')) {
      return mappedImport;
    } else {
      const joinedImport = path.posix.join('/', webModulesWeb, mappedImport);
      if (resolveDependency === 'relative') {
        const relativeToRoot = path.relative(file.opts.filename, file.opts.root);
        const relativeToDir = path
          .relative(path.dirname(webModulesFile), relativeToRoot)
          .replace(/\\/g, '/');
        console.warn('A', file.opts.filename, file.opts.root, relativeToRoot);
        console.warn('B', webModulesFile, path.posix.dirname(webModulesFile), relativeToDir);
        console.warn('BB', relativeToDir + joinedImport);
        return relativeToDir + joinedImport;
      }
      return joinedImport;
    }
  }
  if (isRemoteImport) {
    return imp;
  }
  if (!isSourceImport && !mappedImport) {
    console.log(`warn: bare import "${imp}" not found in import map, ignoring...`);
    return imp;
  }
  if (isSourceImport && shouldResolveRelative) {
    try {
      const dirOfFile = path.dirname(file.opts.filename);
      const absPath = nodeResolve.create.sync({
        extensions: ['.js', '.ts', '.jsx', '.tsx', '.json'],
      })(dirOfFile, imp);
      console.warn('C', file.opts.filename, dirOfFile, imp, absPath);
      const relativePath = path
        .relative(dirOfFile, absPath)
        .replace(/(\.ts|\.tsx|.jsx)$/, '.js')
        .replace(/\\/g, '/');
      return relativePath.startsWith('.') ? relativePath : './' + relativePath;
    } catch (err) {
      // File could not be resolved by Node
      // We warn and just fallback to old 'optionalExtensions' behaviour of appending .js
      console.warn(err.message);
      return imp + '.js';
    }
  }
  return imp;
}

/**
 * BABEL OPTIONS:
 *   dirWeb             - The web_modules installed location once hosted on the web.
 *                        Defaults to "web_modules", which translates absolute imports to "/web_modules/PACKAGE_NAME".
 *   dirFile            - The web_modules installed location on disk. Can be an absolute path, or relative to the current working directory.
 *                        Defaults to "./web_modules".
 *   importMap          - The name/location of the "import-map.json" file generated by Snowpack.
 *                        Relative to the dir path on disk. Defaults to "${dir}/import-map.json".
 *   resolveDependency  - Control how the plugin rewrites bare module, dependency imports. Defaults to "absolute".
 *                        - "absolute": Import dependencies by absolute path (ex: "/web_modules/PACKAGE_NAME")
 *                        - "relative": Import dependencies by relative path on disk (ex: "../web_modules/PACKAGE_NAME")
 *   resolveRelative    - Control how the plugin rewrites relative/local imports. Defaults to "none".
 *                        - "none": Leave relative imports untouched.
 *                        - "node": Rewrite all relative paths to use node's built-in resolution, resolving things like
 *                          missing file extensions and directory imports. This is useful to assist migrating an older,
 *                          bundled project to Snowpack.
 *   dir                - DEPRECATED: use "dirWeb" & "dirFile" instead.
 *   optionalExtensions - DEPRECATED: use "resolve: node" instead.
 */
module.exports = function pikaWebBabelTransform(
  {types: t, env},
  {
    resolveRelative,
    resolveDependency,
    dirFile: webModulesFile,
    dirWeb: webModulesWeb,
    addVersion,
    importMap,
    optionalExtensions,
    dir,
  } = {},
) {
  // Deprecation warnings
  if (dir) {
    console.warn(
      'warn: "dir" option is now deprecated. Please update to use "dirWeb" & "dirFile" instead.',
    );
  }
  if (addVersion) {
    console.warn(
      'warn: "addVersion" option is now built into Snowpack and on by default. The Babel option is no longer needed.',
    );
  }
  // Default options
  webModulesWeb = webModulesWeb || dir || 'web_modules';
  webModulesFile = path.resolve(process.cwd(), webModulesFile || dir || './web_modules');
  resolveDependency = resolveDependency || 'absolute';
  const shouldResolveRelative = resolveRelative === 'node' || !!optionalExtensions;
  // Plugin code
  return {
    pre() {
      this.importMapJson = getImportMap(importMap, webModulesFile);
    },
    visitor: {
      CallExpression(path, {file, opts}) {
        if (path.node.callee.type !== 'Import') {
          return;
        }
        const [source] = path.get('arguments');
        if (source.type !== 'StringLiteral') {
          /* Should never happen */
          return;
        }
        source.replaceWith(
          t.stringLiteral(
            rewriteImport(
              this.importMapJson,
              source.node.value,
              file,
              webModulesFile,
              webModulesWeb,
              shouldResolveRelative,
              resolveDependency,
            ),
          ),
        );
      },
      'ImportDeclaration|ExportNamedDeclaration|ExportAllDeclaration'(path, {file, opts}) {
        const source = path.get('source');
        // An export without a 'from' clause
        if (!source.node) {
          return;
        }
        source.replaceWith(
          t.stringLiteral(
            rewriteImport(
              this.importMapJson,
              source.node.value,
              file,
              webModulesFile,
              webModulesWeb,
              shouldResolveRelative,
              resolveDependency,
            ),
          ),
        );
      },
    },
  };
};
